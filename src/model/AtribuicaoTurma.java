package model;

public class AtribuicaoTurma {

	int id_AtribTurma;
	int id_Atrib;
	int id_Turma;
	
	String nome;
	String turma;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTurma() {
		return turma;
	}
	public void setTurma(String turma) {
		this.turma = turma;
	}
	public int getId_AtribTurma() {
		return id_AtribTurma;
	}
	public void setId_AtribTurma(int id_AtribTurma) {
		this.id_AtribTurma = id_AtribTurma;
	}
	public int getId_Atrib() {
		return id_Atrib;
	}
	public void setId_Atrib(int id_Atrib) {
		this.id_Atrib = id_Atrib;
	}
	public int getId_Turma() {
		return id_Turma;
	}
	public void setId_Turma(int id_Turma) {
		this.id_Turma = id_Turma;
	}
	

}
